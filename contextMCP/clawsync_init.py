def init_project(project_path: Optional[str] = None, overwrite: bool = False) -> Path:
    """
    Initialise the ClawSync Global Context Bridge for a project.

    Parameters
    ----------
    project_path : str | None
        Absolute or relative path to the project root. Defaults to CWD.
    overwrite : bool
        If True, regenerate files even if they already exist.

    Returns
    -------
    Path
        The resolved project root.
    """
    root = resolve_project_root(project_path)
    project_name = root.name

    print(f"\n╔══════════════════════════════════════════════════╗")
    print(f"║  ClawSync — Global Context Bridge Init           ║")
    print(f"╚══════════════════════════════════════════════════╝")
    print(f"  Project  : {project_name}")
    print(f"  Root     : {root}")
    print(f"  Platform : {OS_NAME}")
    print(f"  Time     : {TIMESTAMP}\n")

    openclaw_dir = root / OPENCLAW_DIR
    state_path = openclaw_dir / GLOBAL_STATE_FILE
    task_path = openclaw_dir / ACTIVE_TASK_FILE

    # Relative paths (used in file content)
    state_rel = f"{OPENCLAW_DIR}/{GLOBAL_STATE_FILE}"
    task_rel = f"{OPENCLAW_DIR}/{ACTIVE_TASK_FILE}"

    print("▸ Step 1 — Creating .openclaw/ directory")
    openclaw_dir.mkdir(exist_ok=True)
    print(f"  [OK]    {openclaw_dir}")

    print("\n▸ Step 2 — Writing shared state files")
    safe_write(state_path, _global_state_content(project_name), overwrite)
    safe_write(task_path, _active_task_content(project_name), overwrite)

    # .gitignore entry for registry (cross-platform safe)
    gitignore = root / ".gitignore"
    _ensure_gitignore_entry(gitignore, ".openclaw/registry.json")

    print("\n▸ Step 3 — Creating .cursorrules")
    cursorrules_path = root / CURSORRULES_FILE
    safe_write(
        cursorrules_path,
        _cursorrules_content(state_rel, task_rel),
        overwrite,
    )

    print("\n▸ Step 4 — Patching .vscode/settings.json")
    vscode_settings_path = root / VSCODE_DIR / VSCODE_SETTINGS_FILE
    patch_json_file(vscode_settings_path, _vscode_settings_patch(state_rel, task_rel))

    print(f"\n▸ Step 5 — Writing AGENTS.md")
    agents_path = root / AGENTS_FILE
    safe_write(
        agents_path,
        _agents_md_content(project_name, state_rel, task_rel),
        overwrite,
    )

    print("\n▸ Step 6 — Installing git post-commit hook")
    _install_git_hooks(root, state_rel)

    print(f"\n✅ ClawSync initialised successfully for '{project_name}'!\n")
    print(f"  Next: start the watcher daemon for real-time file-change sync:")
    print(f"  nohup python3 clawsync_watcher.py {root} > ~/.openclaw/watcher.log 2>&1 &\n")
    return root


def _ensure_gitignore_entry(gitignore: Path, entry: str) -> None:
    """Add `entry` to .gitignore if not already present."""
    lines: list[str] = []
    if gitignore.exists():
        lines = gitignore.read_text(encoding="utf-8").splitlines()
    if entry not in lines:
        lines.append(entry)
        gitignore.write_text("\n".join(lines) + "\n", encoding="utf-8")
        print(f"  [OK]    .gitignore updated with '{entry}'")


def _install_git_hooks(root: Path, state_rel: str) -> None:
    """
    Install a git post-commit hook that automatically appends the commit
    info to global_state.md every time `git commit` is run.
    Cross-platform: shell script on Linux/macOS, Python script on Windows.
    """
    git_dir = root / ".git"
    if not git_dir.is_dir():
        print("  [SKIP]  Not a git repo — skipping hook install.")
        return

    hooks_dir = git_dir / "hooks"
    hooks_dir.mkdir(exist_ok=True)
    hook_path = hooks_dir / "post-commit"

    python_exe = sys.executable  # full path to current Python interpreter
    state_abs = str((root / state_rel).resolve()).replace("\\", "/")  # forward slashes

    if OS_NAME == "Windows":
        # On Windows, git uses sh.exe bundled with Git for Windows.
        # We write a shell script that calls Python explicitly.
        hook_content = f"""#!/bin/sh
# ClawSync auto-sync git hook (post-commit)
MSG=$(git log -1 --pretty=%B | head -1)
FILES=$(git diff-tree --no-commit-id -r --name-only HEAD | tr '\\n' ', ' | sed 's/, $//')
TIMESTAMP=$(date '+%Y-%m-%d %H:%M:%S')
ROW="| $TIMESTAMP | Git/Commit | Commit: $MSG — files: $FILES |"
MARKER="<!-- ClawSync appends entries below this line. Most recent = bottom. -->"
python -c "
import sys
p='{state_abs}'
try:
    c=open(p,encoding='utf-8').read()
    r=sys.argv[1]
    m=sys.argv[2]
    if m in c: c=c.replace(m,m+'\\n'+r)
    else: c=c.rstrip()+'\\n\\n'+r+'\\n'
    open(p,'w',encoding='utf-8').write(c)
except Exception as e: print('[ClawSync hook] Error:',e)
" "$ROW" "$MARKER"
"""
    else:
        # Linux / macOS — pure shell, no Python dependency
        hook_content = f"""#!/bin/sh
# ClawSync auto-sync git hook (post-commit)
# Auto-generated by clawsync_init.py — do not edit manually.

STATE_FILE="{state_abs}"
MARKER="<!-- ClawSync appends entries below this line. Most recent = bottom. -->"

if [ ! -f "$STATE_FILE" ]; then
  exit 0
fi

MSG=$(git log -1 --pretty=%B 2>/dev/null | head -1)
FILES=$(git diff-tree --no-commit-id -r --name-only HEAD 2>/dev/null | tr '\n' ',' | sed 's/,$//' | sed 's/,/, /g')
TIMESTAMP=$(date '+%Y-%m-%d %H:%M:%S')
ROW="| $TIMESTAMP | Git/Commit | Commit: $MSG — files: $FILES |"

# Use Python to insert after the marker (handles multiline files safely)
{python_exe} - <<'PYEOF'
import sys
state = "{state_abs}"
marker = "<!-- ClawSync appends entries below this line. Most recent = bottom. -->"
try:
    with open(state, encoding='utf-8') as f:
        content = f.read()
except FileNotFoundError:
    sys.exit(0)

import subprocess, datetime
msg = subprocess.check_output(['git','log','-1','--pretty=%B'], text=True).splitlines()[0]
files_raw = subprocess.check_output(
    ['git','diff-tree','--no-commit-id','-r','--name-only','HEAD'], text=True
).strip().replace('\n', ', ')
ts = datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')
row = f"| {{ts}} | Git/Commit | Commit: {{msg}} — files: {{files_raw}} |"

if marker in content:
    content = content.replace(marker, marker + '\n' + row)
else:
    content = content.rstrip() + '\n\n' + row + '\n'

with open(state, 'w', encoding='utf-8') as f:
    f.write(content)
PYEOF
"""

    # Write hook and make executable
    hook_path.write_text(hook_content, encoding="utf-8")
    if OS_NAME != "Windows":
        hook_path.chmod(0o755)
    print(f"  [OK]    {hook_path}")
    print(f"          → Every 'git commit' will auto-log to {state_rel}")


# ────────────────────────────────────────────────────────────────────────────
# CLI entry point
# ────────────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    import argparse

    parser = argparse.ArgumentParser(
        description="ClawSync — Global Context Bridge Initialiser",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "path",
        nargs="?",
        default=None,
        help="Project root directory (default: current directory)",
    )
    parser.add_argument(
        "--overwrite",
        action="store_true",
        help="Regenerate files even if they already exist",
    )
    args = parser.parse_args()
    init_project(args.path, overwrite=args.overwrite)
