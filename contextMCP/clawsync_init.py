TIMESTAMP = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
OS_NAME = platform.system()  # "Windows" | "Darwin" | "Linux"


# ────────────────────────────────────────────────────────────────────────────
# Helpers
# ────────────────────────────────────────────────────────────────────────────

def resolve_project_root(path: Optional[str] = None) -> Path:
    """Return the absolute project root (defaults to CWD)."""
    root = Path(path).resolve() if path else Path.cwd()
    if not root.is_dir():
        print(f"[ERROR] '{root}' is not a valid directory.")
        sys.exit(1)
    return root


def safe_write(file_path: Path, content: str, overwrite: bool = False) -> None:
    """Write content to a file; skip if exists unless overwrite=True."""
    if file_path.exists() and not overwrite:
        print(f"  [SKIP]  {file_path} already exists.")
        return
    file_path.parent.mkdir(parents=True, exist_ok=True)
    file_path.write_text(content, encoding="utf-8")
    print(f"  [OK]    {file_path}")


def patch_json_file(file_path: Path, updates: dict) -> None:
    """Merge `updates` into an existing (or new) JSON file."""
    file_path.parent.mkdir(parents=True, exist_ok=True)
    existing: dict = {}
    if file_path.exists():
        try:
            existing = json.loads(file_path.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            print(f"  [WARN]  {file_path} has invalid JSON — overwriting.")
    existing.update(updates)
    file_path.write_text(json.dumps(existing, indent=2), encoding="utf-8")
    print(f"  [OK]    {file_path}")


