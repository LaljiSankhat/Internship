#!/usr/bin/env python3
"""
clawsync_init.py — ClawSync Infrastructure Script
===================================================
Bootstraps the Global Context Bridge for a project:
  • Creates .openclaw/ with global_state.md & active_task.md
  • Writes .cursorrules pointing to shared context
  • Patches .vscode/settings.json with Copilot instructions
  • Writes AGENTS.md (the project "Soul" for Antigravity)

Cross-platform: works on Windows, macOS, and Linux.
"""

from __future__ import annotations

import json
import os
import platform
import sys
from datetime import datetime
from pathlib import Path
from typing import Optional


# ────────────────────────────────────────────────────────────────────────────
# Constants
# ────────────────────────────────────────────────────────────────────────────

OPENCLAW_DIR = ".openclaw"
GLOBAL_STATE_FILE = "global_state.md"
